$(function() {
 
    $(window).scroll(function() {
     
        if($(this).scrollTop() != 0) {
        
            $('#toTop').fadeIn();
            $('#HOME').fadeIn();

        
        } else {
        
            $('#toTop').fadeOut();
            $('#HOME').fadeOut();
        
        }
        
    });
        
    $('#toTop').click(function() {
        
        $('body,html').animate({scrollTop:0},800);
        
    });
     
});